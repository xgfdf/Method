public class Stastic {


    public static int f1(int n1,int n2){
        if (n1 > n2) {
            return n1;
        }else return n2;
    }
    public static double f2(int n1,int n2,int n3){
        double res = (n1+n2+n3)/3;
        return res;
    }
    public static int f3(int n1,int n2){
        return n1+n2;
    }


}
